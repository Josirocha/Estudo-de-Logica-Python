# tipos de dados bool (boolean)
# Ao questionar algo em um programa,
# só existe duas respostas possiveis:
# sim(True) ou não(False).
# Existem vários operadores para "questionar"
# dentre eles o ==, que e um operador logico que
# questiona se um valor é igual ao outro.
print(10 == 10) # sim => True (Verdadeiro)
print(10 == 11) # não => False (Falso)
print(type(10 == 10))
print(type(10 == 11))

