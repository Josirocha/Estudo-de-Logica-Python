"""
Python = Lingaugem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
"""
# Aspas simples
print('Josiane Rocha')
print(1,'Josiane "Rocha"')


# Aspas duplas
print("Josiane Rocha")
print(2,"Josiane 'Rocha'")

# Escape
print("Josiane \"Rocha\"")

# r
print(r"Josiane \"Rocha\"")
